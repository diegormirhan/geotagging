<script>
"use strict";
var geocoder;
// This example requires the Places library. Include the libraries=places
// parameter when you first load the API. For example:
// <script src="https://maps.googleapis.com/maps/api/js?key=YOUR_API_KEY&libraries=places">
function initMap() {
	geocoder = new google.maps.Geocoder();
	const map = new google.maps.Map(document.getElementById("map"), {
	  center: {
		lat: -23.5557714,
		lng: -46.6395571
	  },
	  zoom: 9,
	});
	const card = document.getElementById("pac-card");
	const input = document.getElementById("pac-input");
	map.controls[google.maps.ControlPosition.TOP_RIGHT].push(card);
	const autocomplete = new google.maps.places.Autocomplete(input); // Bind the map's bounds (viewport) property to the autocomplete object,
	// so that the autocomplete requests use the current map bounds for the
	// bounds option in the request.

	autocomplete.bindTo("bounds", map); // Set the data fields to return when the user selects a place.

	autocomplete.setFields([
	  "address_components",
	  "geometry",
	  "icon",
	  "name"
	]);
	const infowindow = new google.maps.InfoWindow();
	const infowindowContent = document.getElementById("infowindow-content");
	infowindow.setContent(infowindowContent);
	const marker = new google.maps.Marker({
	  map,
	draggable: true,
	  anchorPoint: new google.maps.Point(0, -29)
	});
	autocomplete.addListener("place_changed", () => {
	  infowindow.close();
	  marker.setVisible(false);
	  const place = autocomplete.getPlace();

	  if (!place.geometry) {
		// User entered the name of a Place that was not suggested and
		// pressed the Enter key, or the Place Details request failed.
		window.alert(
		  "No details available for input: '" + place.name + "'"
		);
		return;
	  } // If the place has a geometry, then present it on a map.

	  if (place.geometry.viewport) {
		map.fitBounds(place.geometry.viewport);
	  } else {
		map.setCenter(place.geometry.location);
		map.setZoom(17); // Why 17? Because it looks good.
	  }

	  marker.setPosition(place.geometry.location);
	  marker.setVisible(true);
	  let address = "";

	  if (place.address_components) {
		address = [
		  (place.address_components[0] &&
			place.address_components[0].short_name) ||
			"",
		  (place.address_components[1] &&
			place.address_components[1].short_name) ||
			"",
		  (place.address_components[2] &&
			place.address_components[2].short_name) ||
			""
		].join(" ");
	  }
	  infowindowContent.children["place-icon"].src = place.icon;
	  infowindowContent.children["place-name"].textContent = place.name;
	  infowindowContent.children["place-address"].textContent = address;
	   var latitude =	place.geometry.location.lat().toFixed(7);
	   var longitude =	place.geometry.location.lng().toFixed(7);
	  infowindowContent.children["place-start"].textContent = latitude;
	  infowindowContent.children["place-end"].textContent = longitude;
	  infowindow.open(map, marker);
		$('#latitude').val(latitude);
		$('#longitude').val(longitude);
	}); // Sets a listener on a radio button to change the filter type on Places
	// Autocomplete.

	google.maps.event.addListener(marker, 'dragend', function(e) {
		geocodePosition(this.getPosition());
		infowindowContent.children["place-icon"].src = '';
		infowindowContent.children["place-name"].textContent = '';
		var latitude =	this.position.lat().toFixed(7);
		var longitude =	this.position.lng().toFixed(7);
		infowindowContent.children["place-start"].textContent = latitude;
		infowindowContent.children["place-end"].textContent = longitude;
		infowindow.open(map, marker);
		$('#latitude').val(latitude);
		$('#longitude').val(longitude);
	}); 
	function geocodePosition(pos) {
	  geocoder.geocode({
		latLng: pos
	  }, function(responses) {
	 console.log(responses);
		let address = "";
		if (responses && responses.length > 0) {
		 address = [
			  (responses[0].address_components[0] &&
				responses[0].address_components[0].short_name) ||
				"",
			  (responses[0].address_components[1] &&
				responses[0].address_components[1].short_name) ||
				"",
			  (responses[0].address_components[2] &&
				responses[0].address_components[2].short_name) ||
				""
			].join(" ");
		} else {
			address = 'Cannot determine address at this location.';
		} 
		infowindowContent.children["place-address"].textContent = address;
	  });
	}
}
